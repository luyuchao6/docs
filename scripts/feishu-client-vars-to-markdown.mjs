function rewriteWikiLinks(md) {
  let out = md;
  const base = 'https://lingdongfangcheng.feishu.cn/wiki/';
  for (const [token, rel] of Object.entries(TOKEN_MAP)) {
    const re = new RegExp(
      '\\]\\(' + base.replace(/[.*+?^${}()|[\]\\]/g, '\\$&') + token + '(?:\\?[^)]*)?\\)',
      'g'
    );
    out = out.replace(re, `](${rel})`);
  }
  return out;
}

